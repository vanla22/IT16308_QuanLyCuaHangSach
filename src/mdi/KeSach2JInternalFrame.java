    public void FilltableSach() {
        DefaultTableModel dtm = (DefaultTableModel) tblSach.getModel();
        dtm.setRowCount(0);
        try {
            String key = txtTimKiem.getText();
            List<Sach> list = sahdao.selectByKeyword(key);
            for (Sach sah : list) {
                Object[] row = {
                    sah.getMaSach(),
                    sah.getTenSach(),
                    sah.getMaKeSach()};

                dtm.addRow(row);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Loi truy van du lieu !");
            e.printStackTrace();
        }
    }
    public void them() {
        int row[] = tblSach.getSelectedRows();
        for (int i = 0; i < row.length; i++) {
            sahdao.updateKeSach(KeSach1JInternalFrame.maksStatic, tblSach.getValueAt(row[i], 0).toString());
        }
        MsgBox.alert(this, "Thêm Thành Công!!");
        this.dispose();
    }
